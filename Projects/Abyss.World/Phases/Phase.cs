namespace Abyss.World.Phases
{
    public static class Phase
    {
        /// <summary>
        /// The calm before the storm.
        /// </summary>
        public const string Calm = "Calm";

        /// <summary>
        /// Unimaginable treasure awaits.
        /// </summary>
        public const string Abundance = "Abundance";

        /// <summary>
        /// Relics become rich with rift energy, allowing their effects to become more potent.
        /// </summary>
        public const string Overpower = "Overpower";

        /// <summary>
        /// Rift energy flows through the player, making them completely invulnerable to harm.
        /// </summary>
        public const string Invulnerable = "Invulnerable";

        /// <summary>
        /// Treasure chests become cursed.
        /// </summary>
        public const string CursedChests = "CursedChests";

        /// <summary>
        /// Items become cursed.
        /// </summary>
        public const string CursedRelics = "CursedRelics";

        /// <summary>
        /// Player is hurt every time they try to shift dimension.
        /// </summary>
        public const string CursedShifts = "CursedShifts";

        /// <summary>
        /// The world is shrouded in darkness.
        /// </summary>
        public const string Darkness = "Darkness";

        /// <summary>
        /// Living entities can reach a terminal velocity in the Abyss.
        /// </summary>
        public const string DeadWeight = "DeadWeight";

        /// <summary>
        /// Picking up rift crystals steals rift from the player.
        /// </summary>
        public const string EmptyPockets = "EmptyPockets";

        /// <summary>
        /// The world inverts; causing entities in the Abyss to fall upwards.
        /// </summary>
        public const string Inversion = "Inversion";

        /// <summary>
        /// Grand columns of lava spurt up from the unknown depths.
        /// </summary>
        public const string LavaFissure = "LavaFissure";

        /// <summary>
        /// Grand columns of lava spurt across the world from out of the void.
        /// </summary>
        public const string LavaTorrent = "LavaTorrent";

        /// <summary>
        /// The player is too heavy to move properly.
        /// </summary>
        public const string LeadBones = "LeadBones";

        /// <summary>
        /// Entities in the Abyss fall at a reduced speed.
        /// </summary>
        public const string LowGravity = "LowGravity";

        /// <summary>
        /// Relics no longer function in the Abyss.
        /// </summary>
        public const string Malfunction = "Malfunction";

        /// <summary>
        /// Entities can no longer shift between dimensions in the Abyss.
        /// </summary>
        public const string Marooned = "Marooned";

        /// <summary>
        /// A phase where extra-dimensional monsters become too volatile to touch, damaging the player if they try to stomp them.
        /// </summary>
        public const string Volatile = "Volatile";

        /// <summary>
        /// The world becomes mirrored, reversing directions.
        /// </summary>
        public const string Mirror = "Mirror";
        
        /// <summary>
        /// The player is only able to see things very close to them.
        /// </summary>
        public const string RiftVeil = "RiftVeil";

        /// <summary>
        /// Curtains of pure rift energy shrouds parts of the world, stopping everything inside them.
        /// </summary>
        public const string ShortSighted = "ShortSighted";

        /// <summary>
        /// A phase where the player is no longer safe from extra-dimensional monsters in Limbo.
        /// </summary>
        public const string LimboWalking = "LimboWalking";

        /// <summary>
        /// Grounded entities move with great pace.
        /// </summary>
        public const string Swiftness = "Swiftness";

        /// <summary>
        /// The world becomes unstable and treacherous to traverse.
        /// </summary>
        public const string Unstable = "Unstable";

        /// <summary>
        /// A phase where extra-dimensional monsters warp to different places periodically.
        /// </summary>
        public const string Vortex = "Vortex";

        /// <summary>
        /// Enormous zones of pure void appear, drawing everything inside them.
        /// </summary>
        public const string VoidZone = "VoidZone";

        /// <summary>
        /// Relics do not give the effect they are supposed to.
        /// </summary>
        public const string Haywire = "Haywire";
    }
}
